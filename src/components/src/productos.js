function loadCategory(categoria) {
    switch (categoria) {
        case "Oki Outfit":
            renderCarrusel(categoria, productosPorCategoria[categoria]);
            break;
        case "Oki Fanmade":
            renderCarrusel(categoria, productosPorCategoria[categoria]);
            break;
        case "Merch Oficial":
            renderCarrusel(categoria, productosPorCategoria[categoria]);
            break;
        default:
            renderCarruseles();
            break;
    }
}

